let currentLang = 'pt';
function setLanguage(lang) {
    currentLang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Toggle active state on language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
    
    document.documentElement.lang = lang === 'pt' ? 'pt-BR' : 'en';
    localStorage.setItem('portfolioLang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    // Determine initial language
    const savedLang = localStorage.getItem('portfolioLang') || 'pt';
    setLanguage(savedLang);
    
    // Add listeners to toggle buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            setLanguage(e.target.getAttribute('data-lang'));
        });
    });
});

// --- Initialize Lenis Smooth Scroll ---
const lenis = new Lenis({
    duration: 1.2,
    easing: (t) => Math.min(1, 1.001 - Math.pow(2, -10 * t)),
    direction: 'vertical',
    gestureDirection: 'vertical',
    smooth: true,
    mouseMultiplier: 1,
    smoothTouch: false,
    touchMultiplier: 2,
    infinite: false,
})

function raf(time) {
    lenis.raf(time)
    requestAnimationFrame(raf)
}
requestAnimationFrame(raf)

// --- Intersection Observer for Reveal Animations ---
const observerOptions = {
    root: null,
    rootMargin: '0px',
    threshold: 0.15
};

const observer = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
        if(entry.isIntersecting) {
            entry.target.classList.add('active');
            observer.unobserve(entry.target);
        }
    });
}, observerOptions);

document.querySelectorAll('.reveal').forEach(element => {
    observer.observe(element);
});

// --- Custom Cursor Implementation ---
const cursorDot = document.querySelector('.cursor-dot');
const cursorGlow = document.querySelector('.cursor-glow');
const links = document.querySelectorAll('a, button, .glass-card');

document.addEventListener('mousemove', (e) => {
    cursorDot.style.transform = `translate(${e.clientX}px, ${e.clientY}px) translate(-50%, -50%)`;
    setTimeout(() => {
        cursorGlow.style.transform = `translate(${e.clientX}px, ${e.clientY}px) translate(-50%, -50%)`;
    }, 50);
});

links.forEach(link => {
    link.addEventListener('mouseenter', () => {
        document.body.classList.add('hovering');
    });
    link.addEventListener('mouseleave', () => {
        document.body.classList.remove('hovering');
    });
});

document.addEventListener('mouseleave', () => {
    cursorDot.style.opacity = '0';
    cursorGlow.style.opacity = '0';
});
document.addEventListener('mouseenter', () => {
    cursorDot.style.opacity = '1';
    cursorGlow.style.opacity = ''; // CSS handles hover opacity
});

// --- Mobile Navigation ---
const menuToggle = document.querySelector('.menu-toggle');
const closeMenu = document.querySelector('.close-menu');
const mobileMenu = document.querySelector('.mobile-menu');
const mobileLinks = document.querySelectorAll('.mobile-link');

function openNav() {
    mobileMenu.classList.add('active');
    lenis.stop();
}

function closeNav() {
    mobileMenu.classList.remove('active');
    lenis.start();
}

if(menuToggle && closeMenu) {
    menuToggle.addEventListener('click', openNav);
    closeMenu.addEventListener('click', closeNav);
    
    mobileLinks.forEach(link => {
        link.addEventListener('click', closeNav);
    });
}

